package ex_04_Java_Operators;

public class Lab040_Relational_Operator
{
    public static void main(String[] args) {
        int a = 2;
        int b = 6;
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a==b);
        System.out.println((a=b));
        System.out.println(a<=b);
        //        1. `<`  (Less than)
//        2. `<=`   (Less than or equal to)
//        3. `>`   (Greater than)
//        4. `>=`   (Greater than or equal to)
//        5. `==`   (Equal to)
//        6. `!=`   (Not equal to)
    }
}
