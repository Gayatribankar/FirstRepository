package ex_04_Java_Operators;

public class Lab041_Logical_Operator
{
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(!a);
        System.out.println(!!a);

//        **Logical Operators ->  (Digital Electronics)**
//        `&&`  (AND)
//        '||`  (OR)
//        `!`  (NOT) - REVERSE
        boolean c = true || false;
        System.out.println(c);

    }
}
